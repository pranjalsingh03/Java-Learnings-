package String;

public class Substring {
    public static void main(String[] args) {

        // substring() method

        String name = "Magan and Pranjal";
        String name2 = name.substring(0, 5);
        String name3 = name.substring(10, 17);
        System.out.println(name); // changed string will be stored in new string
        System.out.println(name2);
        System.out.println(name3);
    }
}
