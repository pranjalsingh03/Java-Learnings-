public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println(c);
        System.out.println(10 + 20); // We can directly solve mathematical equations in print function in Java.
        a += 10;
        System.out.println(a);
        System.out.println(a == b);
        System.out.println(a != b || a == b);
        System.out.println(a != b);
        System.out.println(a == b && a != b);
    }
}