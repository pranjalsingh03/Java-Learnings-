package Operators;

public class increment_1 {

    public static void main(String args[]) {
        int a = 1;
        int b = 2;
        int c = 3;
        a++;
        b--;
        c += 7;
        System.out.println("a is: " + a); // 2
        System.out.println("b is: " + b); // 1
        System.out.println("c is: " + c); // 10
    }
}
