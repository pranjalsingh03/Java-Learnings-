package Boolean;

public class relational_operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println("a==b is: " + (a == b)); // false
        System.out.println("a=b is: " + (a != b)); // true
        System.out.println("a=b is: " + (a > b)); // false
        System.out.println("a=b is: " + (a < b)); // true
        System.out.println("a=b is: " + (a >= b)); // false
        System.out.println("a=b is: " + (a <= b)); // true
    }
}
